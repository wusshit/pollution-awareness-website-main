        <section id="air-pollution">
            <h2>Air Pollution</h2>
            <p>Air pollution is caused by harmful substances such as gases, dust, or chemicals entering the atmosphere. Major sources include emissions from vehicles, factories, and agriculture.</p>
            <h3>Health Impacts</h3>
            <p>Air pollution is responsible for numerous health issues, including chronic respiratory diseases, lung cancer, cardiovascular diseases, and even premature death. It is particularly harmful to children, the elderly, and people with pre-existing health conditions.</p>
            <h3>Disease-Related Family Burdens</h3>
            <p>Families living in areas with poor air quality, especially those in settings with inadequate medical care, bear significant burdens. These include increased healthcare costs, loss of productivity, and, in severe cases, loss of family members.</p>
        </section>

        <section id="water-pollution">
            <h2>Water Pollution</h2>
            <p>Water pollution occurs when harmful substances such as chemicals, plastic, and sewage enter bodies of water. Industrial waste, agricultural runoff, and improper disposal of chemicals are major contributors.</p>
            <h3>Health Impacts</h3>
            <p>Water pollution leads to waterborne diseases like cholera, dysentery, and gastrointestinal infections. Contaminated water affects both human populations and marine ecosystems, leading to biodiversity loss and health crises in vulnerable communities.</p>
            <h3>Disease-Related Family Burdens</h3>
            <p>Families without access to clean water are at constant risk of serious diseases. This not only affects their health but also has economic repercussions, leading to increased medical expenses and a decline in quality of life.</p>
        </section>

        <section id="land-pollution">
            <h2>Land Pollution</h2>
            <p>Land pollution is primarily the contamination of soil by waste disposal, chemicals, and other pollutants. Major sources include industrial waste, improper disposal of garbage, and agricultural chemicals.</p>
            <h3>Health Impacts</h3>
            <p>Exposure to land pollutants, such as heavy metals and pesticides, can cause neurological problems, developmental issues in children, and even cancer. Contaminated soil also reduces food safety, as plants grown in polluted soil absorb toxic substances.</p>
            <h3>Disease-Related Family Burdens</h3>
            <p>Families affected by land pollution often suffer from limited access to safe food, increased health problems, and loss of agricultural productivity. Poor medical facilities in these areas compound these issues, making recovery and prevention difficult.</p>
        </section>

        <section id="future-challenges">
            <h2>Future Challenges</h2>
            <p>The global community faces significant challenges in reducing pollution levels, meeting WHO air quality standards, and managing the economic impacts of pollution. This is particularly relevant in the vehicle market, where transitioning to cleaner energy sources involves high costs, infrastructure changes, and shifts in market dynamics. Many countries, especially poorer nations, struggle to implement regulations that align with WHO standards, which worsens the health outcomes of their populations.</p>
